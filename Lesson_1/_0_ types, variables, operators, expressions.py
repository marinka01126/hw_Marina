"""
    Основные типы данных Python
    примеры в формате:
        инструкция    # результат
        print(4)      # 4
"""

print('Hello world!')
print(type('Hello world'))

print(4)
print(type(4))

print(4,5)
print(type(4.5))

print([1, 2, 3, "hello"])
print(type([1, 2, 3, "hello"]))

print((1, 2, 3, "hello"))
print(type((1, 2, 3, "hello")))

print({5, 2, 2, 3, 3, 1})
print(type({5, 2, 2, 3, 3, 1}))

print({"age": 21})
print(type({"age": 21}))